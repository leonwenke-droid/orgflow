export function t(key: string, locale: Locale = defaultLocale): string {
  const dict = translations[locale] ?? translations.en;
  return dict[key] ?? translations.en[key] ?? key;
}

/** Cookie name used by LocaleProvider for server-side locale. */
export const LOCALE_COOKIE_NAME = "orgflow-locale";

/** Parse locale from cookie value (for server components). */
export function localeFromCookie(cookieValue: string | undefined): Locale {
  if (cookieValue === "de" || cookieValue === "en") return cookieValue;
  return defaultLocale;
}
